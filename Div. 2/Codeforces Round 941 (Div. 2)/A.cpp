using namespace std;

void KyLen() {
    int n,k;
    cin >> n >> k;
    map<int,int> m,cnt;
    for (int i = 1; i <= n; i++) {
        int a;
        cin >> a;
        m[a]++;
    }
    for (auto [x,y] : m) {
        cnt[y]++;
    }
    for (auto [x,y] : cnt) {
        if (x >= k) {
            cout << k - 1 << endl;
            return;
        }
    }
    cout << n << endl;
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}