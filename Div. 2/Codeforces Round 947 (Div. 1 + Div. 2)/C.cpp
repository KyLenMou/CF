#define endl '\n'
#define int long long
using namespace std;

void KyLen() {
    int n;
    cin >> n;
    vector<int> a(n+1);
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }
    int ans = 0;
    for (int i = 2; i <= n; i++) {
        ans = max(ans,min(a[i],a[i-1]));
    }
    for (int i = 3; i <= n; i++) {
        vector<int> t;
        t.push_back(a[i]);
        t.push_back(a[i-1]);
        t.push_back(a[i-2]);
        sort(begin(t),end(t));
        ans = max(ans,t[1]);
    }
    cout << ans << endl;
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}