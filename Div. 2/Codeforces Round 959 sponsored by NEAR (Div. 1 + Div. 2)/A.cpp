using namespace std;

void KyLen() {
    int n,m;
    cin >> n >> m;
    vector<int> a(n*m+1);
    for (int i = 1; i <= n * m; i++) {
        cin >> a[i];
    }
    if (n * m == 1) {
        cout << -1 << endl;
        return;
    }
    for (int i = 1; i <= n * m; i++) {
        a[i]++;
        if (a[i] > n * m) a[i] = 1;
        cout << a[i] << ' ';
        if (i % m == 0) cout << endl;
    }
    
}

