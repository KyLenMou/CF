using namespace std;

void KyLen() {
    int n,k;
    cin >> n >> k;
    if (k == 1 || k > n) {
        cout << n << endl; 
    } else {
        int t = k;
        int cnt = 0;
        while (t * k <= n) {
            t *= k;
        }
        while (n > 0) {
            while (n >= t && n > 0) {
                cnt += (n / t);
                n -= (n / t) * t;
            }
            t /= k;
        }
        cout << cnt << endl;
    }
}

