void KyLen() {
    int a,b;
    cin >> a >> b;
    if (a > b) swap(a,b);
    int ans = 1;
    while (a || b) {
        if ((a & 1) == (b & 1)) {
            ans <<= 1;
            if (a) a >>= 1;
            b >>= 1;
        } else break;
    }
    cout << ans << endl;

}

