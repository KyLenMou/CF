#include<bits/stdc++.h>
#define x first
#define y second
#define endl '\n'
#define int long long
using namespace std;

void KyLen() {
    int n;
    cin >> n;
    vector<int> a(n+1);
    int maxa = 0;
    map<int,vector<int>> m;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        if (a[i] >= maxa) {
            maxa = a[i];
            m[maxa].push_back(i);
        }
    }
    int ans = 0;
    for (auto x : m[maxa]) {
        ans = max(ans, (x - 1) / 2 + (n - x) / 2);
    }
    cout << ans + maxa + 1 << endl; 
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}