using namespace std;

void KyLen() {
    int n,a,b;
    cin >> n >> a >> b;
    int ans = 0;
    if (a >= b) {
        cout << n * a << endl;
        return;
    }
    int k = min(n, b - a - 1);
    ans = max(ans, k * (b + 1) - k * (k + 1) / 2 + a * (n - k));
    k++;
    if (k > n) {
        cout << ans << endl;
        return;
    }
    ans = max(ans, k * (b + 1) - k * (k + 1) / 2 + a * (n - k));
    cout << ans << endl;
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}