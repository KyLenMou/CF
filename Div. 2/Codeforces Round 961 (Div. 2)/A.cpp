void KyLen() {
    int n,k;
    cin >> n >> k;
    int ans = 0;
    if (k >= n) {
        k -= n;
        ans++;
    }
    for (int i = n - 1; i >= 1; i--) {
        if (k >= i) {
            k -= i;
            ans++;
        }
        if (k >= i) {
            k -= i;
            ans++;
        }
        if (k < i) break;
    }
    if (k) ans++;
    cout << ans << endl;
}

