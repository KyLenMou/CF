#define y second
#define endl '\n'
#define int long long
using namespace std;

void KyLen() {
    int n;
    cin >> n;
    vector<int> a(n+1);
    int sum = 0;
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        sum += a[i];
    }
    if (n <= 2) {
        cout << -1 << endl;
        return;
    }
    sort(begin(a)+1,end(a));
    int tar = a[n/2+1];
    int ans = 2 * n * tar - sum;
    if (ans >= 0) cout << ans + 1 << endl;
    else cout << 0 << endl;

}   

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}