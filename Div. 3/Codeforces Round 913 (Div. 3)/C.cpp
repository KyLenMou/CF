void KyLen() {
    int n;
    string s;
    cin >> n >> s;
    s = "?" + s;
    int num = 1;
    map<char,int> m;
    for (int i = 1; i <= n; i++) {
        m[s[i]]++;
        num = max(num,m[s[i]]);
    }
    if (num <= (n + 1) / 2) cout << (n & 1) << endl;
    else {
        cout << num - n + num << endl;
    }
}

