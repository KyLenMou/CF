#include<bits/stdc++.h>
#define x first
#define y second
#define endl '\n'
#define int long long
using namespace std;

void KyLen() {
    int n;
    cin >> n;
    vector<int> a(n+1);
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
        a[i] += a[i-1];
    }
    string s;
    cin >> s;
    s = "?" + s;
    int l = 1, r = n;
    int ans = 0;
    while (l < r) {
        while (l < r && s[l] == 'R') l++;
        while (l < r && s[r] == 'L') r--;
        if (l >= r) break;
        ans += a[r] - a[l-1];
        l++,r--;
    }
    cout << ans << endl;
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}