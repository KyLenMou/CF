using namespace std;

void KyLen() {
    int x,y;
    cin >> x >> y;
    if (y & 1) {
        int num = y / 2 + 1;
        if (x <= (num - 1) * 7 + 11) cout << num << endl;
        else cout << num + (x - ((num - 1) * 7 + 11) + 14) / 15 << endl;
    } else {
        int num = y / 2;
        if (x <= num * 7) cout << num << endl;
        else cout << num + (x - num * 7 + 14) / 15 << endl;
    }
}

