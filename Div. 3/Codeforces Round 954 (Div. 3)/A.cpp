using namespace std;

void KyLen() {
    vector<int> a(3);
    cin >> a[0] >> a[1] >> a[2];
    sort(begin(a),end(a));
    int ans = 1e9;
    for (int i = a[0]; i <= a[2]; i++) {
        ans = min(ans,abs(a[0]-i)+abs(a[1]-i)+abs(a[2]-i));
    }
    cout << ans << endl;
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}