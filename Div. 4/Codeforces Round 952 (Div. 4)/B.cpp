#define y second
#define endl '\n'
#define int long long
using namespace std;

void KyLen() {
    int n;
    cin >> n;
    int ans = 0;
    int cur = 0;
    for (int i = 2; i <= n; i++) {
        int p = 1;
        int num = 0;
        while (p * i <= n) {
            num += p * i;
            p++;
        }
        if (num > ans) {
            ans = num;
            cur = i;
        }
    }
    cout << cur << endl;
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(0), cout.tie(0);
    int T; cin >> T; while(T--) KyLen();
    return 0;
}